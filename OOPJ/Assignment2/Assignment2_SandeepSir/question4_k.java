public class question4_k {  
    public static void main(String[] args) {          
        int i = 7;  
        System.out.println("Number = " + i);  
        //returns the string representation of the integer value in binary form  
        System.out.println("Binary representation is = " + Integer.toBinaryString(i));
        System.out.println("Octal representation is = " + Integer.toOctalString(i));
        System.out.println("Hexadecimal representation is = " + Integer.toHexString(i));
        
    }  
}  